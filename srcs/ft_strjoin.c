#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	size;
	char	*ptr;

	size = ft_strlen(s1) + ft_strlen(s2) + 1;
	if (!(ptr = (char *)ft_calloc(sizeof(*ptr),  size)))
		return (NULL);
	ft_memcpy(ptr, s1, ft_strlen(s1));
	ft_memcpy(ptr + ft_strlen(s1), s2, ft_strlen(s2));
	return(ptr);
}
